-- FASE 5
-- Creacion de una Base de Datos

-- Creo esquema y lo selecciono
CREATE SCHEMA CinemExtract;
USE CinemExtract;

-- Tabla Fase 1: Datos de API de MoviesDataset
CREATE TABLE peliculas_generos(
	id_pelicula INT NOT NULL AUTO_INCREMENT,
    tipo VARCHAR(255) NOT NULL,
    titulo VARCHAR(255) NOT NULL,
    anio INT, 
	mes INT,
    id_imdb VARCHAR (255),
    genero VARCHAR(255),
    PRIMARY KEY (id_pelicula)
);

-- Tabla Fase 2: Detalles de Peliculas con Selenium de IMDB
CREATE table detalles_peliculas(
	id_detalles INT NOT NULL AUTO_INCREMENT,
	id_imdb VARCHAR(255),
	puntuacion FLOAT,
	porcentaje INT,
	direccion VARCHAR(255),
	guion VARCHAR(255),
	argumento MEDIUMTEXT,
	genero VARCHAR(255),
	duracion INT,
	titulo VARCHAR(255),
    PRIMARY KEY (id_detalles)
);

-- Tabla Fase 3: Detalles de Actores con Selenium de IMDB
CREATE table actores(
	id_actor INT NOT NULL AUTO_INCREMENT,
	nombre_actor VARCHAR(200) NOT NULL,
	anio_nac INT,
	conocido_por VARCHAR(200),
	que_hace VARCHAR(200),
	premios_ganados INT,
	PRIMARY KEY (id_actor)
);

-- Tabla Fase 4: Premios Oscar con Beautiful Soup de Wikipedia
CREATE table oscars(
	id_ceremonia INT NOT NULL AUTO_INCREMENT,
	año_ceremonia INT NOT NULL,
	mejor_pelicula VARCHAR(255) NOT NULL,
	mejor_director VARCHAR(255) NOT NULL,
	mejor_actor VARCHAR(255) NOT NULL,
	mejor_actriz VARCHAR(255) NOT NULL,
    PRIMARY KEY (id_ceremonia)
);


